public class Program3 {
	public static void main(String[] args) {
		boolean p = true;
		boolean q = false;
		System.out.println("p = " + p);
		System.out.println("q = " + q);
		System.out.println(); // пустая строка

		System.out.println("p && q = " + (p && q));
		System.out.println("p || q = " + (p || q));
		System.out.println("p ^ q = " + (p ^ q));
		System.out.println("!p = " + !p);
		System.out.println("!q = " + !q);
	}
}